#' Multiplicación entre dos números
#'
#' Esta función devuelve el producto de dos números
#'
#' @param a Primer número
#' @param b Segundo número
#'
#' @return El producto entre a y b
#' @export
#'
#' @examples
#' mult(5,3)
#'
#' @seealso \link{div}, \link{suma}
mult=function(a,b){
  return(a*b)
}
